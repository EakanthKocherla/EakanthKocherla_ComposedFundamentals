## Data Types in Python ##

#Subscripting
print("Hello"[0]) #Prints the First character

#Negative subscripting
print("Hello"[-1]) #Prints the Last character

#String
print("Hello") #Prints the string "Hello"

#String concatenation
print("Hello" + " World") #Concatenates two strings

#Integer
print(5) #Prints the integer 5

#Large Number Integer
print(1_23_45_67_89_01_23_45_67_890) #Prints a large integer,_ underscores are ignored. _can be used for readability

#Float
print(3.14) #Prints the float 3.14

#Large Number Float
print(1_23_45_67_89_01_23_45_67.890) #Prints a large float, underscores are ignored. _ can be used for readability

#Boolean
print(True)  # Prints the boolean value True
print(False) # Prints the boolean value False